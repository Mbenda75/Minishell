#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t				i;
	unsigned char		*dest;
	unsigned char		*src2;

	i = 0;
	dest = (unsigned char *)dst;
	src2 = (unsigned char *)src;
	while (i < n)
	{
		dest[i] = src2[i];
		if (src2[i] == (unsigned char)c)
			return (dst + i + 1);
		i++;
	}
	return (NULL);
}
